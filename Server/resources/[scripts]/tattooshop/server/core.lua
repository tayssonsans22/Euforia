local Tunnel = module("vrp", "lib/Tunnel")
local Proxy = module("vrp", "lib/Proxy")
vRP = Proxy.getInterface("vRP")
-----------------------------------------------------------------------------------------------------------------------------------------
-- CONNECTION
-----------------------------------------------------------------------------------------------------------------------------------------
Hensa = {}
Tunnel.bindInterface("tattooshop", Hensa)
-----------------------------------------------------------------------------------------------------------------------------------------
-- UPDATE
-----------------------------------------------------------------------------------------------------------------------------------------
function Hensa.Update(Tattoos)
	local source = source
	local Passport = vRP.Passport(source)
	if Passport then
		local Tables = json.encode(Tattoos)
		if Tables ~= "[]" then
			vRP.Query("playerdata/SetData", { Passport = Passport, Name = "Tatuagens", Information = Tables })
		end
	end
end